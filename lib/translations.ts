// All available translations
const translations = {
  en,
  uz,
  ru,
}

export function useTranslations() {
  const [language, setLanguage] = useState<string>("en")

  useEffect(() => {
    // Get the language from localStorage if available
    const storedLanguage = localStorage.getItem("language")
    if (storedLanguage && translations[storedLanguage as keyof typeof translations]) {
      setLanguage(storedLanguage)
    }
  }, [])

  const t = (key: string): string => {
    const translationSet = translations[language as keyof typeof translations]
    return translationSet[key as keyof typeof translationSet] || key
  }

  return t
}
